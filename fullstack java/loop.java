public class loop {
    public static void main(String[] args) {
        for(int i=0; i<=5; i++){
            for(int j=0;j<=6;j++){
                System.out.println("i+" +i);
        }
    }
    int n=1;
    while(n<10){
        System.out.println("n="+n);
        n++;
    }
    int i=1;
    do{
        System.out.println("i="+i);
        i++;
    }while(i<10);
    
 }
    
}
